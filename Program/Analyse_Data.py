def Compute_Prob(path, showCount = False):
    """
    Estimate the probabilities of obtaining a 0 or a 1 in the sequence
    based on their observed frequencies. The longer the sequence,
    the higher the confidence in this estimate.

    If showCount=True, this function also displays the difference
    between the number of 0s and 1s.
    """
    zCount = 0
    oCount = 0
    bitCount = 0
    
    try:
        with open(path,"r") as f: # To avoid overflow
            strArr = f.read(readStep)
            currLen = len(strArr)
            while currLen != 0:
                curr_oCount = strArr.count('1')
                oCount += curr_oCount
                zCount += currLen-curr_oCount
                
                bitCount += currLen
                currBit = f.read(readStep)
                currLen = len(currBit)
    except:
        print(f"ERROR: File was not found in path \n {path}")
        return None,None
    if showCount:
        print(f"0s: {zCount} ({zCount*100/bitCount}), 1s: {oCount} ({oCount*100/bitCount}), bits: {bitCount}, diffPer: {(oCount-zCount)/bitCount}")
    # Calculamos las probabilidades
    zProb  = zCount/bitCount
    oProb = 1-zProb
    return zProb,oProb

def Estimate_Hmin(fileName):
    """
    It is sufficient to consider the most probable outcome in the sequence.

    We use the definition

    Hmin = -log(Max(P_i))

    TODO: Determine how to quantify the confidence of the estimate.
    """
    maxP = max(Compute_Prob(fileName))
    return -np.log2(maxP)

def Estimate_H(fileName):
    probs = Compute_Prob(fileName)
    return -probs[0]*np.log2(probs[0]) - probs[1]*np.log2(probs[1])
