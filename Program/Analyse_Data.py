def AWS_Cost_Function(nBits,cs,ct,sMax,sMin,nqbits):
    """
    Cost_Function:
        Calculates the cost.
    """
    # It must work between sMin and sMax. For intermediate values of shots, it should be infinite
    #if nBits > nqbits: nqbits = nBits
    nShots = nBits//nqbits
    nShots = np.where(nShots == 0, 1,nShots) # If we are in the case of requesting fewer bits than qubits
    T = nShots//sMax # Count the number of required tasks
    R = nShots%sMax
    rLess_sMin = R < sMin
    # When R < sMin, one additional task is required and the number of shots does not change
    # (it is now compatible by distributing sMax + R between two tasks)
    f = np.where(rLess_sMin,ct*(T+2) + cs*(sMax*T + R),ct*(T+1) + cs*(sMax*T + R)) # The +1 in T is because task 0 is also counted
    f = np.where(nShots < 1, ct + cs*nShots ,f) # Specific case of requesting fewer bits than qubits
    return f

def IBM_Cost_Function(nBits,ratio, cost_perSecond):
    timeNeeded = nBits/ratio 
    return cost_perSecond*timeNeeded

